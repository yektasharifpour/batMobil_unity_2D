# Bat Man Runner Game

---

## 🎮 Game Description (English)

This game features a running Bat Man character who can move left and right.
The player starts as a runner and, upon reaching the Batmobile, can enter and drive it.

By pressing the **O** key, Bat Man can exit the Batmobile and return to runner mode.

The Batmobile includes multiple modes and abilities that can be activated using different keys.

---

## ⌨️ Controls

### Bat Man Runner
-**Arrow Right / D * * : Move Right  
- **Arrow Left / A** : Move Left  
- **O** : Exit Batmobile  

### Batmobile
- **B** : Activate Bat Signal  
- **E** : Deactivate Bat Signal  
- **C** : Stealth Mode(dim lights)
-**Space * * : Alert Mode(blinking red & blue lights + siren)
-**Left Shift / Right Shift * * : Speed Boost  


---

## 🕹 Gameplay Flow
1. Control Bat Man and move left or right.
2. Reach the Batmobile to enter the vehicle.
3. Use different keys to activate Batmobile abilities.
4. Press **O** to exit the Batmobile and continue on foot.
5. Switch between Normal, Stealth, and Alert modes during gameplay.

---

## 🧾 Summary of Implemented Features (خلاصه کار انجام‌شده)

- An existing **background asset** has been used for the environment, which moves dynamically along with the camera.
- Core functionalities for the **Batmobile movement** and **speed control** have been implemented.
- Multiple vehicle abilities have been added, including:
  -Activating the** Bat Signal**
  - Dimming the lights in **Stealth Mode**
  - Changing light colors and playing a **siren sound** in **Alert Mode**
  - A **Normal Mode** with no visual or audio changes
- A separate script has been created for the **Bat Man character**, handling:
  -Left and right movement
  - Entering and exiting the Batmobile
- The **rotating Bat Signal animation** was designed frame by frame using **Figma * *.
- The * *Bat Man character animation** was created by converting a GIF into individual frames using **Adobe Photoshop * *, then importing and assembling them as an animation inside **Unity**.

