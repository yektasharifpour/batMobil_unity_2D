# Bat Man Runner Game

---

## 🎮 توضیح بازی (Persian)

این بازی درباره‌ی یک **بت‌من دونده** است که بازیکن می‌تواند او را به چپ و راست حرکت دهد.  
در ابتدای بازی، بت‌من به‌صورت پیاده حرکت می‌کند و پس از رسیدن به **بت‌موبیل**، وارد ماشین می‌شود.

بت‌موبیل دارای قابلیت‌ها و حالت‌های مختلفی است که با استفاده از دکمه‌های مختلف فعال می‌شوند:

-با فشردن دکمه **B**، علامت بت‌من (Bat Signal) فعال می‌شود.
- با فشردن دکمه **D**، علامت بت‌من غیرفعال می‌شود.
- با فشردن دکمه **C**، ماشین وارد حالت **مخفی‌کاری (Stealth Mode)** می‌شود و نور چراغ‌ها کم‌رنگ می‌گردد.
- با فشردن دکمه **Space**، ماشین وارد حالت **هشدار (Alert Mode)** می‌شود:
  -چراغ‌ها به‌صورت چشمک‌زن قرمز و آبی تغییر می‌کنند
  - صدای آژیر پخش می‌شود
- با نگه داشتن دکمه **Shift**، سرعت حرکت ماشین افزایش پیدا می‌کند.

---

## 🎮 Game Description (English)

This game features a running Bat Man character who can move left and right.
The player starts as a runner and, upon reaching the Batmobile, can enter and drive it.

The Batmobile includes multiple modes and abilities that can be activated using different keys.

---

## ⌨️ Controls

### Bat Man Runner
-**Arrow Right / D * * : Move Right  
- **Arrow Left / A** : Move Left  

### Batmobile
- **B** : Activate Bat Signal  
- **D** : Deactivate Bat Signal  
- **C** : Stealth Mode(dim lights)
-**Space * * : Alert Mode(blinking red & blue lights + siren)
-**Left Shift / Right Shift * * : Speed Boost  

---

## 🕹 Gameplay Flow
1. Control Bat Man and move left or right.
2. Reach the Batmobile to enter the vehicle.
3. Use different keys to switch between Normal, Stealth, and Alert modes.
4. Take advantage of speed boost and visual effects during gameplay.

