# Bat Man Runner Game

---



## 🎮 Game Description (English)

This game features a running Bat Man character who can move left and right.
The player starts as a runner and, upon reaching the Batmobile, can enter and drive it.

By pressing the **O** key, Bat Man can exit the Batmobile and return to runner mode.

The Batmobile includes multiple modes and abilities that can be activated using different keys.

---

## ⌨️ Controls

### Bat Man Runner
-**Arrow Right / D * * : Move Right  
- **Arrow Left / A** : Move Left  

### Batmobile
- **B** : Activate Bat Signal  
- **E** : Deactivate Bat Signal  
- **C** : Stealth Mode(dim lights)
-**Space * * : Alert Mode(blinking red & blue lights + siren)
-**Left Shift / Right Shift * * : Speed Boost  
- **O** : Exit Batmobile  

---

## 🕹 Gameplay Flow
1. Control Bat Man and move left or right.
2. Reach the Batmobile to enter the vehicle.
3. Use different keys to activate Batmobile abilities.
4. Press **O** to exit the Batmobile and continue on foot.
5. Switch between Normal, Stealth, and Alert modes during gameplay.

